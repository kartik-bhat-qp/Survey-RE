export const DEFAULT_SURVEY_CREATION_LANGUAGE = 'en-us';

export function getSurveyCreationLanguageLabel(value: string): string {
  return (
    SURVEY_CREATION_LANGUAGES.find((language) => language.value === value)?.label ??
    'English (United States)'
  );
}

export function getSurveyCreationLanguageShortLabel(value: string): string {
  const match = SURVEY_CREATION_LANGUAGES.find((language) => language.value === value);
  if (!match) return 'EN-US';
  const [languageCode, regionCode] = match.value.split('-');
  return `${languageCode.toUpperCase()}-${regionCode.toUpperCase()}`;
}
